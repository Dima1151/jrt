package com.javarush.task.task01.task0138;

/* 
Любимое стихотворение

В методе main напиши код, чтобы в результате выполнения программы было выведено твое любимое стихотворение.
В стихотворении должно быть не меньше, чем 4 строчки. Не убирай вывод строчки «Мое любимое стихотворение:».
*/

public class Solution {
    public static void main(String[] args) {
        System.out.println("Мое любимое стихотворение:");
        System.out.println();
        System.out.println("Она была с Ним больше полугода.");
        System.out.println("Держа себя - как руль - в Его руках.");
        System.out.println("Он властно вел себя и путеводно.");
        System.out.println("Указывая ей - куда и как.");
    }
}